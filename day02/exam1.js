이름상자2               // 변수명 작성하여 변수값 호출
console.log(이름상자2)  // 변수명 작성하여 변수값 호출하고 console에 log(출력)

// 변수의 값 수정 : 누구를 어떻게 수정할 것인가
// 변수명 = 새로운 값

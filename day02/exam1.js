// 같은 파일 내 변수명은 중복이 안 된다. 지역변수, 변역변수
/*
1. let 
2. 변수/상자/공간 이름 = 변수명 = 카멜표기법(권장) = mycar -> myCar
3. 대입연산자를 이용하여 초기값 설정 가능
*/

let 이름상자1  // '이름상자' 변수 선언, *초기값 없음
